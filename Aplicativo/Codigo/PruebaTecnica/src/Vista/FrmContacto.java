    private void txttel_conKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txttel_conKeyPressed
        
//Validar para que solo reciba numeros
        txttel_con.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if (!((Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)))) {
                    getToolkit().beep();
                    e.consume();
                }
            }
        });
    }//GEN-LAST:event_txttel_conKeyPressed

