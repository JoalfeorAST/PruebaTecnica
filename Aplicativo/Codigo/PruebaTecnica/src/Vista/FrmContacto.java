    //método para validar las cajas de texto y llama al método guardar
    public void validar() {
        if (txtnom_con.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Falto digitar el nombre");
            txtnom_con.grabFocus();

        } else if (txttel_con.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Falto digitar el telefono");
            txttel_con.grabFocus();

        } else if (txtema_con.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Falto digitar el email");
            txtema_con.grabFocus();

        } else if (txtasu_con.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Falto digitar el asunto");
            txtasu_con.grabFocus();

        } else if (txtmen_con.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Falto digitar el mensaje");
            txtmen_con.grabFocus();

        } else {
            this.captura();
            contacto.guardar();
        }
    }    

    public void captura() {
            contacto.setNom_con(txtnom_con.getText());
            contacto.setTel_con(Integer.parseInt(txttel_con.getText()));            
            contacto.setEma_con(txtema_con.getText());
            contacto.setAsu_con(txtasu_con.getText());
            contacto.setMen_con(txtmen_con.getText());
    }    
